package estruturas_selecao;

public class SelecaoMultiplaEscolha {
	 public static void main (String[] args)
     {
       int codigo = 2; //Código escolhido.
       
       switch (codigo)
       {
         case 1 : System.out.println("O produto é um perfume.");
         break;
         case 2 : System.out.println("O produto é um shampoo.");
         break;
         case 3 : System.out.println("O produto é um pente.");
         break;
         case 4 : System.out.println("O produto é um sabonete.");
         break;
       }
     }
   }

